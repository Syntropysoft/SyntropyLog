/**
 * Post-build patch: replace NAPI-RS default isMusl() that uses execSync('which ldd')
 * with a version that uses only fs.existsSync and process.env.PATH (no shell).
 * Run after `napi build` so the published index.js never contains child_process.execSync.
 */
const fs = require('fs');
const path = require('path');

const indexPath = path.join(__dirname, '..', 'index.js');
let content = fs.readFileSync(indexPath, 'utf8');

const needsJsPatch = content.includes("require('child_process').execSync") || content.includes('execSync');
if (!needsJsPatch) {
  console.log('patch-index-no-shell: index.js already has no execSync, skip js patch');
}

if (needsJsPatch) {
  // 0) Add runtime comment after "auto-generated by NAPI-RS" so the file documents no subprocess
  if (!content.includes('Runtime: no subprocess')) {
    content = content.replace(
      /(\/\* auto-generated by NAPI-RS \*\/)/,
      '$1\n/* Runtime: no subprocess execution. Only (1) existsSync for .node under __dirname, (2) resolveLddPathWithoutShell + readFileSync of ldd for musl on Linux. See SECURITY.md in syntropylog. */'
    );
  }

  // 1) Replace the execSync line with safe resolveLddPathWithoutShell() call (allow single or double quotes from NAPI-RS)
  content = content.replace(
    /const lddPath = require\s*\(\s*['"]child_process['"]\s*\)\s*\.execSync\s*\(\s*['"]which ldd['"]\s*\)\s*\.toString\s*\(\s*\)\s*\.trim\s*\(\s*\)/,
    'const lddPath = resolveLddPathWithoutShell()\n      if (!lddPath) return true // assume musl on unknown'
  );

  // 2) Insert resolveLddPathWithoutShell() before "switch (platform)" so it's defined when isMusl() runs
  const resolveLddPathBlock = `
/** Resolve path to ldd using PATH and fs.existsSync only (no subprocess). Reads process.env.PATH; documented in syntropylog SECURITY.md. */
function resolveLddPathWithoutShell() {
  const candidates = ['/usr/bin/ldd']
  const pathEnv = process.env.PATH
  if (pathEnv) {
    for (const dir of pathEnv.split(':')) {
      if (dir) candidates.push(join(dir.trim(), 'ldd'))
    }
  }
  for (const p of candidates) {
    try {
      if (existsSync(p)) return p
    } catch (_) {}
  }
  return null
}

`;

  content = content.replace(/\nswitch\s*\(\s*platform\s*\)/, resolveLddPathBlock + 'switch (platform)');

  if (content.includes('execSync')) {
    console.error('patch-index-no-shell: execSync still present after patch');
    process.exit(1);
  }

  fs.writeFileSync(indexPath, content, 'utf8');
  console.log('patch-index-no-shell: removed execSync from index.js, added resolveLddPathWithoutShell()');
}

// 3) Patch index.mjs: use static require('./index.js') instead of require(join(__dirname, 'index.js')) (avoids fs/path/url and dynamic path alerts)
const indexMjsPath = path.join(__dirname, '..', 'index.mjs');
if (fs.existsSync(indexMjsPath)) {
  let mjs = fs.readFileSync(indexMjsPath, 'utf8');
  const hasDynamicRequire = /require\s*\(\s*join\s*\(\s*__dirname\s*,\s*['"]index\.js['"]\s*\)\s*\)/.test(mjs);
  if (hasDynamicRequire) {
    mjs = `import { createRequire } from 'node:module'

const require = createRequire(import.meta.url)
// Static path: load the CJS sibling (no dynamic require; safe for tooling/Socket)
const native = require('./index.js')

export const { configureNative, fastSerialize, fastSerializeFromJson, ping } = native
export default native
`;
    fs.writeFileSync(indexMjsPath, mjs, 'utf8');
    console.log('patch-index-no-shell: index.mjs now uses static require("./index.js")');
  }
}
